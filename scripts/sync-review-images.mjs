import fs from "node:fs/promises";
import { readFileSync } from "node:fs";
import path from "node:path";
import { createClient } from "@supabase/supabase-js";

const rootDir = process.cwd();
const reviewDir = path.join(rootDir, "public/review-section");
const generatedDir = path.join(rootDir, "src/generated");
const generatedFile = path.join(generatedDir, "review-images.ts");

const supportedExtensions = new Set([".webp", ".png", ".jpg", ".jpeg", ".avif"]);

function loadEnv() {
  try {
    const lines = readFileSync(".env", "utf8");
    const vars = {};
    for (const line of lines.split("\n")) {
      const trimmed = line.trim();
      if (!trimmed || trimmed.startsWith("#")) continue;
      const eqIdx = trimmed.indexOf("=");
      if (eqIdx === -1) continue;
      vars[trimmed.slice(0, eqIdx).trim()] = trimmed.slice(eqIdx + 1).trim();
    }
    return vars;
  } catch {
    return {};
  }
}

async function listSupabaseImages() {
  const env = loadEnv();
  const supabaseUrl = env.VITE_SUPABASE_URL;
  const supabaseKey = env.VITE_SUPABASE_PUBLISHABLE_KEY;

  if (!supabaseUrl || !supabaseKey) {
    return [];
  }

  try {
    const supabase = createClient(supabaseUrl, supabaseKey);
    const { data, error } = await supabase
      .from("testimonials_images")
      .select("image_url")
      .order("created_at", { ascending: true });

    if (error || !data) return [];
    return data.map((row) => row.image_url);
  } catch {
    return [];
  }
}

async function syncReviewImages() {
  await fs.mkdir(reviewDir, { recursive: true });
  await fs.mkdir(generatedDir, { recursive: true });

  let entries;
  try {
    entries = await fs.readdir(reviewDir, { withFileTypes: true });
  } catch {
    entries = [];
  }

  const localImages = entries
    .filter((entry) => {
      if (!entry.isFile()) return false;
      if (entry.name.startsWith(".")) return false;
      return supportedExtensions.has(path.extname(entry.name).toLowerCase());
    })
    .map((entry) => `/review-section/${entry.name}`)
    .sort((a, b) => a.localeCompare(b, undefined, { numeric: true, sensitivity: "base" }));

  const supabaseImages = await listSupabaseImages();

  const allImages = [...localImages, ...supabaseImages];

  const content = `/* eslint-disable */
// This file is auto-generated by scripts/sync-review-images.mjs.
// Do not edit manually.

export const reviewImages = ${
    JSON.stringify(allImages, null, 2)
  } as const;
`;

  await fs.writeFile(generatedFile, content, "utf8");
  console.log(`Synced ${allImages.length} review images → src/generated/review-images.ts`);
}

const isDirectRun =
  process.argv[1] &&
  path.resolve(process.argv[1]) === path.resolve(new URL(import.meta.url).pathname);

if (isDirectRun) {
  syncReviewImages().catch((error) => {
    console.error(error);
    process.exitCode = 1;
  });
}
