import fs from "node:fs/promises";
import path from "node:path";

const rootDir = process.cwd();
const artworkRoot = path.join(rootDir, "public/assets/artwork");
const featuredRoot = path.join(rootDir, "public/assets/featured");
const generatedDir = path.join(rootDir, "src/generated");
const generatedFile = path.join(generatedDir, "artwork-manifests.ts");
const categoryIndexFile = path.join(artworkRoot, "categories.json");
const supportedExtensions = new Set([".webp", ".png", ".jpg", ".jpeg", ".avif"]);

const titleOverrides = new Map([
  ["dnd", new Map([["arcane-duelist", "Arcane Duelist"]])],
  ["furry", new Map([["neon-pathfinder", "Neon Pathfinder"]])],
  ["anime", new Map([["skyline-prodigy", "Skyline Prodigy"]])],
  ["warhammer", new Map([["iron-vanguard", "Iron Vanguard"]])],
  ["group-art", new Map([["guild-assembly", "Guild Assembly"]])],
]);

const readJson = async (filePath) => {
  const content = await fs.readFile(filePath, "utf8");
  return JSON.parse(content);
};

const normalizeSlug = (value) => value.trim().toLowerCase();

const humanizeFileName = (categorySlug, fileName) => {
  const slug = fileName.replace(path.extname(fileName), "");
  const override = titleOverrides.get(categorySlug)?.get(slug);

  if (override) {
    return override;
  }

  return slug
    .replace(/[_-]+/g, " ")
    .replace(/\s+/g, " ")
    .trim()
    .replace(/\b\w/g, (char) => char.toUpperCase());
};

const listArtworkFiles = async (categorySlug) => {
  const categoryDir = path.join(artworkRoot, categorySlug);
  const entries = await fs.readdir(categoryDir, { withFileTypes: true });

  return entries
    .filter((entry) => {
      if (!entry.isFile()) {
        return false;
      }

      if (entry.name.startsWith(".")) {
        return false;
      }

      if (entry.name === "manifest.json") {
        return false;
      }

      return supportedExtensions.has(path.extname(entry.name).toLowerCase());
    })
    .sort((a, b) =>
      a.name.localeCompare(b.name, undefined, {
        numeric: true,
        sensitivity: "base",
      }),
    )
    .map((entry) => ({
      title: humanizeFileName(categorySlug, entry.name),
      image: `/assets/artwork/${categorySlug}/${entry.name}`,
    }));
};

const listFeaturedFiles = async (categorySlug) => {
  const categoryDir = path.join(featuredRoot, categorySlug);

  try {
    const entries = await fs.readdir(categoryDir, { withFileTypes: true });

    return entries
      .filter((entry) => {
        if (!entry.isFile() || entry.name.startsWith(".")) {
          return false;
        }

        return supportedExtensions.has(path.extname(entry.name).toLowerCase());
      })
      .sort((a, b) =>
        a.name.localeCompare(b.name, undefined, {
          numeric: true,
          sensitivity: "base",
        }),
      )
      .map((entry) => `/assets/featured/${categorySlug}/${entry.name}`);
  } catch {
    return [];
  }
};

const writeManifestJson = async (categorySlug, items) => {
  const manifestPath = path.join(artworkRoot, categorySlug, "manifest.json");
  const payload = JSON.stringify({ slug: categorySlug, items }, null, 2);
  await fs.writeFile(manifestPath, `${payload}\n`, "utf8");
};

const generateTypeScriptModule = (categories, manifests) => {
  const summaries = categories.map((category) => ({
    ...category,
    slug: normalizeSlug(category.slug),
  }));

  return `/* eslint-disable */
// This file is auto-generated by scripts/sync-artwork-manifests.mjs.
// Do not edit manually.

import type {
  ArtworkCategoryManifest,
  ArtworkCategorySlug,
  ArtworkCategorySummary,
} from "@/data/artwork";

export const generatedArtworkCategories = ${JSON.stringify(
    summaries,
    null,
    2,
  )} as const satisfies ReadonlyArray<ArtworkCategorySummary>;

export const generatedArtworkManifests = ${JSON.stringify(
    manifests,
    null,
    2,
  )} as const satisfies Record<ArtworkCategorySlug, ArtworkCategoryManifest>;
`;
};

export async function syncArtworkManifests() {
  const categories = await readJson(categoryIndexFile);
  const manifests = {};
  const generatedCategories = [];

  for (const category of categories) {
    const categorySlug = normalizeSlug(category.slug);
    const items = await listArtworkFiles(categorySlug);
    const featuredFiles = await listFeaturedFiles(categorySlug);
    const featuredImage = featuredFiles[0] ?? items[0]?.image ?? category.featuredImage;
    manifests[categorySlug] = { slug: categorySlug, items };
    generatedCategories.push({
      ...category,
      slug: categorySlug,
      heroImage: items[0]?.image ?? category.heroImage,
      featuredImage,
    });
    await writeManifestJson(categorySlug, items);
  }

  await fs.mkdir(generatedDir, { recursive: true });
  await fs.writeFile(
    generatedFile,
    generateTypeScriptModule(generatedCategories, manifests),
    "utf8",
  );
}

const isDirectRun =
  process.argv[1] &&
  path.resolve(process.argv[1]) === path.resolve(new URL(import.meta.url).pathname);

if (isDirectRun) {
  syncArtworkManifests().catch((error) => {
    console.error(error);
    process.exitCode = 1;
  });
}
